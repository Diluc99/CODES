#include <iostream>
using namespace std;
int main()
{       
    int num_i=12;
    float num_f=12.22;
    double num_d=12.449589288;
    char ch='a';
    cout<<"The size of int variable is: "<<sizeof(num_i)<<endl;
    cout<<"The size of float variable is: "<<sizeof(num_f)<<endl;
    cout<<"The size of double variable is: "<<sizeof(num_d)<<endl;
    cout<<"The size of char variable is: "<<sizeof(ch)<<endl;
return 0;
}